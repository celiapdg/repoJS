function sumaTres(a, b, c) {
    return a + b + c;
}

console.log(sumaTres(1, 2, 3));

function nombreCompleto(nombre, apellido1, apellido2) {
    console.log(nombre + " " + apellido1 + " " + apellido2);
}

function maximo(a, b) {
    if (b > a) {
        return b;
    } else {
        return a;
    }
}

console.log(maximo(5, 2));